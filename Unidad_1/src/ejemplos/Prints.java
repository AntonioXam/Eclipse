package ejemplos;

public class Prints {

	public static void main(String[] args) {
	   System.out.println("Hola");
	   System.out.println(2+2);
	   System.out.println("2+2");
	   
	   System.out.println("2+2="+4);
	   System.out.println("2+2="+"4");
	   System.out.println("2+2=4");
	   
	   System.out.println();
	   
	   System.out.println("2+2="+2+2);
	   System.out.println("2+2="+(2+2));
	   
	   System.out.println();
	   
	   System.out.println(4+3+4);
	   System.out.println(2+"3"+4);
	   

	}

}
