package Ejercicios;

public class Empleado {

	private int sueldo;

	/**
	 * @param sueldo
	 */
	public Empleado(int sueldo) {
		this.sueldo = sueldo;
	}

	/**
	 * @return el sueldo
	 */
	public int getSueldo() {
		return sueldo;
	}

	/**
	 * @param sueldo el sueldo a establecer
	 */
	public void setSueldo(int sueldo) {
		this.sueldo = sueldo;
	}
	
	
}
