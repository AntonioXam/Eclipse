package ejemplos;

public class Operacionesaritmeticas2 {

	public static void main(String[] args) {
		//Declaración de variable 
		int a;
		double b;
		
		//Inicialización de variables
		a=10;
		b=3;
		
		
		System.out.println("a="+a);
		System.out.println("b="+b);

		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b));
		System.out.println("a%b="+(a%b));
		
	}

}
