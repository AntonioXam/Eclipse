package ejemplos;

public class Casting {

	public static void main(String[] args) {
	int a=8;
	double b=15.35;
	
	System.out.println(a);
	System.out.println(b);
	
	System.out.println();
	
	System.out.println((float)a);
	System.out.println((int)b);
	
	System.out.println();
	
	System.out.println(a);
	System.out.println(b);
	
	System.out.println();
	
	a=(int)b;
	
	System.out.println(a);
	
	
	
		
	}

}
