package pruebascasa;



	public class BucleForEjemplo {
	    public static void main(String[] args) {
	        /*En este código:

int i = 1: Inicializa la variable i con el valor 1.
i <= 5: La condición que indica que el bucle debe seguir ejecutándose mientras i sea menor o igual a 5.
i++: Incrementa el valor de i en cada iteración.
El resultado de este programa sería imprimir:
	         */
	        for (int i = 6; i <= 99; i+=7) {
	            System.out.println("Número: " + i);
	        }
	    }
	}


